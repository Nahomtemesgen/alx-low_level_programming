#include "main.h"
#include <stdio.h>
#include <string.h>
/**
 * _strcpy - function that copies the string
 * @dest: parameter that specify the position of the copy
 * @src: parameter that search the character
 * Return: Always 0
 */
char *_strcpy(char *dest, char *src)
{
strcpy(dest, src);
return (dest);
}
